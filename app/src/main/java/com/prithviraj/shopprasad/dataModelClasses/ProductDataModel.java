package com.prithviraj.shopprasad.dataModelClasses;

public class ProductDataModel {
    int productId, price, discountPercentage, quantity;
    String productName, productImage;
    int vendorId;
    String vendorName;

    public ProductDataModel() {
    }

    public ProductDataModel(int productId, int price, int discountPercentage, String productName, String productImage) {
        this.productId = productId;
        this.price = price;
        this.discountPercentage = discountPercentage;
        this.productName = productName;
        this.productImage = productImage;
    }

    public int getVendorId() {
        return vendorId;
    }

    public void setVendorId(int vendorId) {
        this.vendorId = vendorId;
    }

    public String getVendorName() {
        return vendorName;
    }

    public void setVendorName(String vendorName) {
        this.vendorName = vendorName;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getDiscountPercentage() {
        return discountPercentage;
    }

    public void setDiscountPercentage(int discountPercentage) {
        this.discountPercentage = discountPercentage;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductImage() {
        return productImage;
    }

    public void setProductImage(String productImage) {
        this.productImage = productImage;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
